export function pixelWidgetTitle(id: string): string {
  const item = getDrawerCatalog(localeState.current).items.find((w) => w.id === id);
  return item?.name ?? id;
}

export function pixelWidgetDesc(id: string): string {
  const item = getDrawerCatalog(localeState.current).items.find((w) => w.id === id);
  return item?.desc ?? '';
}
